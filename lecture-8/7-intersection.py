''' Intersection of A and B is a set of elements that are common in both sets.
    Intersection is performed using & operator. Same can be accomplished using the method
    intersection()  '''
    
# initialize A and B
A = {1, 2, 3, 4, 5}
B = {4, 5, 6, 7, 8}

# use & operator
# Output: {4, 5}
print(A & B)